package junit_p1;

public class Calculator {
    /**
     * addition
     * @param a
     * @param b
     * @return a + b
     */
    public int add(int a, int b){
        return a + b;
    }

    /**
     * sub
     * @param a
     * @param b
     * @return a + b
     */
    public int sub(int a, int b){
        return a - b;
    }


}
